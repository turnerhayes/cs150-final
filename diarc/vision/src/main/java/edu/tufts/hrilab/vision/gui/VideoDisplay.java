    /** Creates new form VideoDisplay */
    public VideoDisplay(final Dimension size) {
        initComponents();

        imagePanel = new JImgPanel();
        imagePanel.setSize(size);
        imagePanel.setBounds(0, 0, size.width, size.height);
        imagePanel.setVisible(true);
        this.setVisible(true);
        this.setSize(size);
        this.add(imagePanel);
    }

    public void displayImage(BufferedImage img) {
        imagePanel.displayImage(img);
    }

    private class JImgPanel extends JPanel {

        private static final long serialVersionUID = 1L;
        BufferedImage localImg;

        public void displayImage(BufferedImage img) {
            localImg = img;
            repaint();
        }

        @Override
        public void paint(Graphics g) {
            super.paint(g);
            if (localImg != null) {
                g.drawImage(localImg, 0, 0, null);
            }
        }
    }

